def birthdayCakeCandles(candles):
    max_height_candles = max(sorted(list(set(candles))))
    no_of_tallest_candles = 0
    for i in candles:
        if i == max_height_candles: no_of_tallest_candles += 1
    return no_of_tallest_candles

